#include <unistd.h>

int	main(int ac, char *av[])
{
	int	i = 0, test = 42;

	if (ac == 2)
	{
		while(av[1][i] == ' ' || av[1][i] == '\t')
			i++;
		while(av[1][i])
		{
			if (av[1][i] == ' ' || av[1][i] == '\t')
				test = -42;
			if (!(av[1][i] == ' ' || av[1][i] == '\t'))
			{
				if (test == -42)
					write (1, " ",1);
				write (1, &av[1][i], 1);
				test = 0;
			}
			i++;
		}
	}
	write (1, "\n", 1);
	return (0);
}
