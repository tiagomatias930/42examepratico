int		max(int* tab, unsigned int len)
{
	int	max, i = 0;

	if (len == 0)
		return (0);
	max = tab[0];

	for(i = 0; i <= len; i++)
	{
		if (max < tab[i])
			max = tab[i];
	}
	return (max);
}
